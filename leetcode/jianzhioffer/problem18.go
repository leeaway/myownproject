func deleteNode(head *ListNode, val int) *ListNode {
	dummy := head
	if head.Val == val {
		return head.Next
	}
	pre := head
	head = head.Next
	for head != nil {
		if head.Val != val {
			pre = head
			head = head.Next
			continue
		}
		pre.Next = head.Next
		break
	}
	return dummy
}
